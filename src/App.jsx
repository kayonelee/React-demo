import { useEffect, useState } from "react"
import { NewTodoForm } from "./NewTodoForm"
import "./styles.css"
import { TodoList } from "./TodoList"

export default function App() {
  const [todos, setTodos] = useState(() => {
    const localValue = localStorage.getItem("ITEMS")
    if (localValue == null) return []

    return JSON.parse(localValue)
  })
  {/**
  *THE LOCALSTORAGE.SETITEM() METHOD IS USED TO STORE THE TODOS ARRAY IN THE LOCAL STORAGE
  *UNDER THE KEY "ITEMS." BY USING LOCALSTORAGE.SETITEM(), THE TODO ITEMS ARE PERSISTED IN
  *THE BROWSER'S LOCAL STORAGE, ENSURING THEY ARE AVAILABLE EVEN IF THE USER REFRESHES OR
  *CLOSES THE PAGE.
  */}

  useEffect(() => {
    localStorage.setItem("ITEMS", JSON.stringify(todos))
  }, [todos])

  function addTodo(title) {
    setTodos(currentTodos => {
      return [
        ...currentTodos,
        { id: crypto.randomUUID(), title, completed: false },
      ]
    })
  }

  function toggleTodo(id, completed) {
    setTodos(currentTodos => {
      return currentTodos.map(todo => {
        if (todo.id === id) {
          return { ...todo, completed }
        }

        return todo
      })
    })
  }

  function deleteTodo(id) {
    setTodos(currentTodos => {
      return currentTodos.filter(todo => todo.id !== id)
    })
  }

  return (
    <>
      {/**
      *RENDERING ADDING COMPONENT-IMPORTING NEWTODOFORM.JSX COMPONENT
      * CAPITAL N LETTER LETS REACT KNOW ITS A CUSTOM COMPONENT WE CREATED  
      */}
      <NewTodoForm onSubmit={addTodo} />
      <h1 className="header">Todo List</h1>
      <TodoList todos={todos} toggleTodo={toggleTodo} deleteTodo={deleteTodo} />
    </>
  )
}

